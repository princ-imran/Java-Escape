package javaescape;

/**
 *
 * @author Spy Hacker
 */
public class JavaEscape {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.print("Name: Imran \b Hoshain");//Use for backspace
        System.out.print("\nAge: 26");//Use for new line
        System.out.println("Semister: 3rd \tUniversity: Canadian University of Bangladesh");//Use for single tab or step
        System.out.println("Imran \r Hoshain"); //Use for Carriage Return
        System.out.println("\"Imran Hoshain\""); //Use for ""
        System.out.println("\\Imran Hoshain\'"); //Use \ and '
        
    }
    
}
